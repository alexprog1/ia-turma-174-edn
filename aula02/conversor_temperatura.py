# 🧊🔥 Conversor de Temperatura: Celsius ➜ Fahrenheit

# Solicita ao usuário a temperatura em Celsius
celsius = float(input("Digite a temperatura em °C (graus Celsius): "))

# Converte Celsius para Fahrenheit usando a fórmula oficial
fahrenheit = (celsius * 9 / 5) + 32

# Exibe o resultado formatado de forma clara e amigável
print(f"\n🌡️ {celsius:.1f}°C equivale a {fahrenheit:.1f}°F")
